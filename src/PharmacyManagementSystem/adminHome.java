    public void profilePic() {
        try {
            con = pharmd.connectDB();
            pst = con.prepareStatement("select * from users where email=?");
            pst.setString(1, emaill);
            rs=pst.executeQuery();
            if(rs.next()){
                byte[] img=rs.getBytes("image");
                ImageIcon imag=new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(130, 153, Image.SCALE_SMOOTH));
                jLabel2.setIcon(imag);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                pst.close();
                rs.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idd = jLabel1.getText();
        String name = jTextField1.getText();
        String email = jTextField2.getText();
        String phone = jTextField3.getText();
        String pass = jPasswordField1.getText();
        SimpleDateFormat sf = new SimpleDateFormat("dd-MM-yyyy");
        Date date = jDateChooser1.getDate();
        String d = "";
        if (date != null) {
            d = sf.format(jDateChooser1.getDate());
        }
        if (name.isBlank()) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Name field <br> is required</b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else if (email.isBlank()) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Email field <br> is required</b></html>", "Error message", JOptionPane.ERROR_MESSAGE);
        } else if (!email.matches(emailPattern)) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Invalid Email address</b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else if (phone.isBlank()) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Phone field <br> is reqired</b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else if (phone.length() != 10) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Invalid Phone number</b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else if (pass.isBlank()) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Password field is required</b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else if (d.isBlank()) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Date field <br> is required</b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else if (gender == null) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Gender field is required</b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else if (jLabel9.getIcon() == null) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Attach your image </b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                con = pharmd.connectDB();
                pst = con.prepareStatement("insert into users(idd,role,name,email,password,phone,dob,gender,status,image)values(?,?,?,?,?,?,?,?,?,?)");
                pst.setString(1, idd);
                pst.setString(2, "user");
                pst.setString(3, name);
                pst.setString(4, email);
                pst.setString(5, pass);
                pst.setString(6, phone);
                pst.setString(7, d);
                pst.setString(8, gender);
                pst.setString(9, "inactive");
                pst.setBytes(10, person_image);
                pst.executeUpdate();
                //ImageIcon icon = new ImageIcon("src\\image\\correct.png");
                JOptionPane.showMessageDialog(null, "<html><b  style=\"font-size:12px;\">Account created <br> SUCCESSFULLY</b></html> ", "Message", JOptionPane.INFORMATION_MESSAGE);

                clearFields();
                autoID();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


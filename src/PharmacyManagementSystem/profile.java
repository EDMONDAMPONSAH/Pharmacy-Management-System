    private void loadProfile() {
        try {
            con = pharmd.connectDB();
            pst = con.prepareStatement("select * from users where email = '" + email + "'");
            rs = pst.executeQuery();
            if (rs.next()) {
                jTextField2.setText(rs.getString("name"));
                jPasswordField1.setText(rs.getString("password"));
                jTextField6.setText(rs.getString("phone"));
                SimpleDateFormat sf = new SimpleDateFormat("dd-MM-yyyy");
                java.util.Date date = sf.parse(rs.getString("dob"));
                jDateChooser1.setDate(date);
                if (rs.getString("gender").equals("Male")) {
                    jComboBox1.removeAllItems();
                    jComboBox1.addItem("Male");
                    jComboBox1.addItem("Female");
                } else {
                    jComboBox1.removeAllItems();
                    jComboBox1.addItem("Female");
                    jComboBox1.addItem("Male");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }


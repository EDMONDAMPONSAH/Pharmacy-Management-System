    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField2.getText();
        SimpleDateFormat sf = new SimpleDateFormat("dd-MM-yyyy");
        String dob = sf.format(jDateChooser1.getDate());
        String pass = jPasswordField1.getText();
        String phone = jTextField6.getText();
        String gender = jComboBox1.getSelectedItem().toString();

        try {
            con = pharmd.connectDB();
            pst = con.prepareStatement("update users set name =? ,dob =? ,password =? ,phone =? ,gender =? where email=?");
            pst.setString(1, name);
            pst.setString(2, dob);
            pst.setString(3, pass);
            pst.setString(4, phone);
            pst.setString(5, gender);
            pst.setString(6, email);
            pst.executeUpdate();
            //ImageIcon icon = new ImageIcon("src\\image\\correct.png");
            JOptionPane.showMessageDialog(null, "<html><b  style=\"font-size:12px;\">Profile updated <br> SUCCESSFULLY</b></html> ", "Message", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed


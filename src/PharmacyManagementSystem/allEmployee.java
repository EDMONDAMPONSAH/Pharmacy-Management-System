    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        String id = dft.getValueAt(selectedIndex, 0).toString();
        int delete = JOptionPane.showConfirmDialog(null, "Do you really want to remove ' " + dft.getValueAt(selectedIndex, 1).toString() + " '?", "Delete Emplloyee", JOptionPane.YES_NO_OPTION);
        if (delete == 0) {
            try {
                con = pharmd.connectDB();
                pst = con.prepareStatement("delete from users where idd = ?");
                pst.setString(1, id);
                pst.executeUpdate();
                loadTable("");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            DateTimeFormatter df = DateTimeFormatter.ofPattern("dd-MM-yyyy");
            LocalDateTime now = LocalDateTime.now();
            String currentDate = df.format(now);
            String query = "select * from users where role = 'user'";
            con = pharmd.connectDB();
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();
            JFileChooser chooser = new JFileChooser();
            chooser.setSelectedFile(new File("All Employees" + "  " + currentDate + ".pdf"));
            int chooserDialog = chooser.showSaveDialog(null);
            if (chooserDialog == JFileChooser.APPROVE_OPTION) {
                filepath = chooser.getSelectedFile().getPath();
            }

            Document mydocument = new Document();
            PdfWriter.getInstance(mydocument, new FileOutputStream(filepath));
            PdfPTable table = new PdfPTable(6);
            mydocument.open();
            float[] columnWidth = new float[]{3, 7, 7, 5, 5, 5};
            table.setWidths(columnWidth);
            table.setWidthPercentage(100);

            mydocument.add(new Paragraph("                                  EMPLOYEE LIST", FontFactory.getFont(FontFactory.TIMES_BOLD, 20, Font.BOLD)));
            mydocument.add(new Paragraph(" "));
            mydocument.add(new Paragraph(" "));
            table.addCell(new PdfPCell(new Paragraph("ID", FontFactory.getFont(FontFactory.TIMES_ROMAN, 12, Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Name", FontFactory.getFont(FontFactory.TIMES_ROMAN, 12, Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Email", FontFactory.getFont(FontFactory.TIMES_ROMAN, 12, Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Phone", FontFactory.getFont(FontFactory.TIMES_ROMAN, 12, Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Gender", FontFactory.getFont(FontFactory.TIMES_ROMAN, 12, Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Date of Birth", FontFactory.getFont(FontFactory.TIMES_ROMAN, 12, Font.BOLD))));

            while (rs.next()) {
                table.addCell(new PdfPCell(new Paragraph(rs.getString("idd"), FontFactory.getFont(FontFactory.TIMES_ROMAN, 10, Font.PLAIN))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("name"), FontFactory.getFont(FontFactory.TIMES_ROMAN, 10, Font.BOLD))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("email"), FontFactory.getFont(FontFactory.TIMES_ROMAN, 10, Font.BOLD))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("phone"), FontFactory.getFont(FontFactory.TIMES_ROMAN, 10, Font.BOLD))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("gender"), FontFactory.getFont(FontFactory.TIMES_ROMAN, 10, Font.BOLD))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("dob"), FontFactory.getFont(FontFactory.TIMES_ROMAN, 10, Font.BOLD))));

            }
            mydocument.add(table);
            mydocument.add(new Paragraph(" "));
            mydocument.add(new Paragraph(currentDate));
            mydocument.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed



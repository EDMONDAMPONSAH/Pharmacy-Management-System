    private void searc() {
        String id = jTextField4.getText();
        try {
            con = pharmd.connectDB();
            pst = con.prepareStatement("select * from medicine where idd = '" + id + "'");
            rs = pst.executeQuery();
            if (rs.next()) {
                jTextField4.setEditable(false);
                jTextField2.setText(rs.getString("name"));
                jTextField3.setText(rs.getString("company"));
                SimpleDateFormat sf = new SimpleDateFormat("yyyy-MM-dd");
                java.util.Date date = sf.parse(rs.getString("expired"));
                jDateChooser1.setDate(date);
                jDateChooser1.setEnabled(false);
                jSpinner1.setValue(Integer.valueOf(rs.getString("qty")));
                jTextField6.setText(rs.getString("ppu"));

            } else {
                //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
                JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">' " + id + " ' not found", "Message", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }


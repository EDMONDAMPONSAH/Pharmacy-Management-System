    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField4.getText();
        String name = jTextField2.getText();
        String company = jTextField3.getText();
        String ppu = jTextField6.getText();
        String qty = jSpinner1.getValue().toString();
        if (jTextField2.getText().isBlank() || jTextField3.getText().isBlank() || jTextField6.getText().isBlank() || jSpinner1.getValue().equals("")) {
            //ImageIcon icon = new ImageIcon("src\\image\\Incorrect Password.png");
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Fill blank field</b></html>", "Error Message", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                con = pharmd.connectDB();
                pst = con.prepareStatement("update medicine set name=? ,company=? , qty=? , ppu=? where idd = ?");
                pst.setString(1, name);
                pst.setString(2, company);
                pst.setString(3, qty);
                pst.setString(4, ppu);
                pst.setString(5, id);
                pst.executeUpdate();
                //ImageIcon icon = new ImageIcon("src\\image\\correct.png");
                JOptionPane.showMessageDialog(null, "<html><b  style=\"font-size:12px;\">' " + name + " ' has been <br> updated SUCCESSFULLY</b></html> ", "Message", JOptionPane.INFORMATION_MESSAGE);
                refreshPage();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed


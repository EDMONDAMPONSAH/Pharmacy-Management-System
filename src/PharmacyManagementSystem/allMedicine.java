    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        try {

            DateTimeFormatter df = DateTimeFormatter.ofPattern("dd-MM-yyyy");
            LocalDateTime now = LocalDateTime.now();
            String currentDate = df.format(now);
            String query = "select * from medicine";
            con = pharmd.connectDB();
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();
            JFileChooser chooser = new JFileChooser();
            chooser.setSelectedFile(new File("List of Medicine" + " " + currentDate + ".pdf"));
            int chooserDialog = chooser.showSaveDialog(null);
            if (chooserDialog == JFileChooser.APPROVE_OPTION) {
                filepath = chooser.getSelectedFile().getPath();
            }
            Document mydocument = new Document();
            PdfWriter.getInstance(mydocument, new FileOutputStream(filepath));
            PdfPTable table=new PdfPTable(6);
            mydocument.open();
            float[] columnWidth=new float[]{3,7,7,5,5,5};
            table.setWidths(columnWidth);
            table.setWidthPercentage(100);
            
            mydocument.add(new Paragraph("                                  LIST OF MEDICINE",FontFactory.getFont(FontFactory.TIMES_BOLD,20,Font.BOLD)));
            mydocument.add(new Paragraph(" "));
            mydocument.add(new Paragraph(" "));
            table.addCell(new PdfPCell(new Paragraph("ID",FontFactory.getFont(FontFactory.TIMES_ROMAN,12,Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Name",FontFactory.getFont(FontFactory.TIMES_ROMAN,12,Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Company",FontFactory.getFont(FontFactory.TIMES_ROMAN,12,Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Quantity",FontFactory.getFont(FontFactory.TIMES_ROMAN,12,Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Price Per Unit",FontFactory.getFont(FontFactory.TIMES_ROMAN,12,Font.BOLD))));
            table.addCell(new PdfPCell(new Paragraph("Expiry Date",FontFactory.getFont(FontFactory.TIMES_ROMAN,12,Font.BOLD))));
            
            while(rs.next()){
                table.addCell(new PdfPCell(new Paragraph(rs.getString("idd"),FontFactory.getFont(FontFactory.TIMES_ROMAN,10,Font.PLAIN))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("name"),FontFactory.getFont(FontFactory.TIMES_ROMAN,10,Font.PLAIN))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("company"),FontFactory.getFont(FontFactory.TIMES_ROMAN,10,Font.PLAIN))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("qty"),FontFactory.getFont(FontFactory.TIMES_ROMAN,10,Font.PLAIN))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("ppu"),FontFactory.getFont(FontFactory.TIMES_ROMAN,10,Font.PLAIN))));
                table.addCell(new PdfPCell(new Paragraph(rs.getString("expired"),FontFactory.getFont(FontFactory.TIMES_ROMAN,10,Font.PLAIN))));
            }
            mydocument.add(table);
            mydocument.add(new Paragraph(" "));
            mydocument.add(new Paragraph(currentDate));
            mydocument.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


